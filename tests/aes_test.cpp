#include <gtest/gtest.h>
#include <crypto/cipher/Aes.h>
#include <crypto/cipher/Gcm.h>


TEST(aes, encryption) {
    uint8_t block[] = {0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xaa, 0xbb, 0xcc, 0xdd, 0xee, 0xff};
    Key key({0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e,0x0f});

    uint8_t encrypted_block[16];
    Aes aes(key);
    aes.encrypt(block, encrypted_block);

    uint8_t expected_block[] = {0x69, 0xC4, 0xE0, 0xD8, 0x6A, 0x7B, 0x04, 0x30, 0xD8, 0xCD, 0xB7, 0x80, 0x70, 0xB4, 0xC5, 0x5A};
    for(size_t i = 0; i < 16; i++)
        GTEST_ASSERT_EQ(encrypted_block[i], expected_block[i]);
}

TEST(aes, decryption) {
    uint8_t encrypted_block[] = {0x69, 0xC4, 0xE0, 0xD8, 0x6A, 0x7B, 0x04, 0x30, 0xD8, 0xCD, 0xB7, 0x80, 0x70, 0xB4, 0xC5, 0x5A};
    Key key({0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e,0x0f});

    uint8_t block[16];
    Aes aes(key);
    aes.decrypt(encrypted_block, block);

    uint8_t expected_block[] = {0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xaa, 0xbb, 0xcc, 0xdd, 0xee, 0xff};
    for(size_t i = 0; i < 16; i++)
        GTEST_ASSERT_EQ(block[i], expected_block[i]);
}
